class Movie:
    def __init__(self, movie_id, movie_name, movie_genre, movie_description, movie_director, movie_actors, movie_language, movie_category):
        self.movie_id = movie_id
        self.movie_name = movie_name
        self.movie_genre = movie_genre
        self.movie_description = movie_description
        self.movie_director = movie_director
        self.movie_actors = movie_actors
        self.movie_language = movie_language
        self.movie_category = movie_category